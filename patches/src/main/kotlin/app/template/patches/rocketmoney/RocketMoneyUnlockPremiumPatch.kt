private val hermesMagic = bytesOf(0xC6, 0x1F, 0xBC, 0x03, 0xC1, 0x03, 0x19, 0x1F)

private fun ByteArray.requireHermes(path: String) {
    if (size < 32 || !copyOfRange(0, hermesMagic.size).contentEquals(hermesMagic)) {
        throw PatchException("Invalid Hermes bytecode bundle: $path")
    }

    val version = readLittleEndianInt(8)
    if (version != 96) {
        throw PatchException("Unsupported Rocket Money Hermes bytecode version: $version")
    }
}

private fun ByteArray.readLittleEndianInt(offset: Int): Int {
    var value = 0
    for (index in 0 until 4) {
        value = value or ((this[offset + index].toInt() and 0xFF) shl (index * 8))
    }
    return value
}

private fun ByteArray.withUpdatedSha1(): ByteArray {
    val content = copyOf(size - 20)
    return content + MessageDigest.getInstance("SHA-1").digest(content)
}

private fun ByteArray.findUnique(pattern: ByteArray, name: String): Int {
    var found = -1
    val last = size - pattern.size

    outer@ for (start in 0..last) {
        for (index in pattern.indices) {
            if (this[start + index] != pattern[index]) continue@outer
        }
        if (found >= 0) {
            throw PatchException("$name signature is ambiguous.")
        }
        found = start
    }

    if (found < 0) {
        throw PatchException("$name signature not found.")
    }
    return found
}

private fun bytesOf(vararg values: Int) =
    values.map { it.toByte() }.toByteArray()
